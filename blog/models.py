
from django.db import models

class Post(models.Model):
    title = models.CharField(max_length=200)
    description = models.TextField(blank=True)  # você já deve ter
    content = models.TextField()               # HTML geral, como já está

    ingredients = models.TextField(blank=True)  # NOVO
    preparation = models.TextField(blank=True)  # NOVO

    image = models.ImageField(upload_to='posts/', blank=True, null=True)
    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return self.title

